package operadores;

public class Ternario {
    public static void main(String[] args) {
        //Operadores  ternário
        //Operação  é  verdadeiro ? então é falso
        int a = 3,b = 5;
        String isTrue = a == b ? "Sim, é verdadeiro" : "É falso";
        System.out.println(isTrue);
    }
}
